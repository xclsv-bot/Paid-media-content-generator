#!/usr/bin/env python3
"""Generate supabase/seed.sql from a client's paid-media sheet.

One-off authoring tool: reads the Excel export and emits idempotent seed SQL for
concept_families + creatives, scoped to one org (see
supabase/migrations/0015_organizations.sql). Re-run if the sheet changes.
Usage: python3 scripts/generate_seed.py /path/to/sheet.xlsx [--org-slug outlier]
"""
import sys
import datetime
import openpyxl

LABELS = {"Narrative", "Audience", "Strategic intent",
          "✓ Proven hook formula", "⚠ Compliance"}


def q(v):
    if v is None or (isinstance(v, str) and not v.strip()):
        return "null"
    if isinstance(v, bool):
        return "true" if v else "false"
    if isinstance(v, (datetime.datetime, datetime.date)):
        return "'" + v.strftime("%Y-%m-%d") + "'"
    return "'" + str(v).strip().replace("'", "''") + "'"


def archetype_of(meta):
    m = (meta or "").lower()
    has_q, has_b = "qualifier" in m, "broad" in m
    if has_q and has_b:
        return "Mixed"
    if has_q:
        return "Qualifier"
    if has_b:
        return "Broad-appeal"
    if "mixed" in m:
        return "Mixed"
    return None


def main(path, org_slug="outlier"):
    wb = openpyxl.load_workbook(path, data_only=True)

    # ---- concept families ----
    cf = wb["Concept Families"]
    families, cur = [], None
    for row in cf.iter_rows(values_only=True):
        a = row[0]
        b = row[1] if len(row) > 1 else None
        if a is None or not str(a).strip():
            continue
        a = str(a).strip()
        if a.startswith("Concept Families"):
            continue
        if a in LABELS:
            if not cur:
                continue
            key = a
            if key == "Narrative":
                cur["narrative"] = b
            elif key == "Audience":
                cur["audience"] = b
            elif key == "Strategic intent":
                cur["intent"] = b
            elif key == "✓ Proven hook formula":
                cur["hook"] = b
            elif key == "⚠ Compliance":
                cur["compliance"] = b
        else:
            cur = {"name": a, "meta": b, "narrative": None, "audience": None,
                   "intent": None, "hook": None, "compliance": None}
            families.append(cur)

    # ---- content pieces ----
    cp = wb["Content Pieces"]
    rows = list(cp.iter_rows(values_only=True))
    hdr = [str(h).strip() if h else "" for h in rows[0]]
    idx = {h: i for i, h in enumerate(hdr)}
    creatives = []
    for r in rows[1:]:
        if r[idx["ID"]] is None:
            continue
        creatives.append(r)

    org_sql = "(select id from public.organizations where slug = %s)" % q(org_slug)

    out = []
    out.append("-- AUTO-GENERATED by scripts/generate_seed.py — do not edit by hand.")
    out.append("-- Seeds concept_families + creatives into the '%s' org." % org_slug)
    out.append(
        "-- This script only ever seeds one org at a time, so concept_family_id\n"
        "-- lookups below are unscoped by org_id — safe for a single run, not a\n"
        "-- pattern to copy elsewhere.\n"
    )

    out.append("with org as (select id from public.organizations where slug = %s)" % q(org_slug))
    out.append("insert into public.concept_families")
    out.append("  (org_id, name, archetype, narrative, audience, strategic_intent, proven_hook_formula, is_proven, compliance_note)")
    out.append(
        "select org.id, v.name, v.archetype::archetype_type, v.narrative, v.audience, v.strategic_intent, v.proven_hook_formula, v.is_proven, v.compliance_note\n"
        "from org, (values"
    )
    vals = []
    for f in families:
        is_proven = bool(f["hook"]) or "proven" in (f["meta"] or "").lower()
        vals.append("  (%s, %s, %s, %s, %s, %s, %s, %s)" % (
            q(f["name"]), q(archetype_of(f["meta"])), q(f["narrative"]),
            q(f["audience"]), q(f["intent"]), q(f["hook"]),
            "true" if is_proven else "false", q(f["compliance"]),
        ))
    out.append(",\n".join(vals))
    out.append(") as v(name, archetype, narrative, audience, strategic_intent, proven_hook_formula, is_proven, compliance_note)")
    out.append("on conflict (org_id, name) do nothing;\n")

    for r in creatives:
        def g(col):
            i = idx.get(col)
            return r[i] if i is not None else None
        fam = g("Concept Family")
        vd = g("Variant Differentiator")
        is_proven = "proven" in (str(vd or "")).lower()
        out.append(
            "insert into public.creatives\n"
            "  (sheet_id, org_id, concept_family_id, content_summary, hook_line, hook_angle,\n"
            "   archetype, feature_pillar, sport, format, variant_differentiator, cta, is_proven, delivery_date)\n"
            "values (%s, %s, (select id from public.concept_families where name = %s),\n"
            "   %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s)\n"
            "on conflict (sheet_id, org_id) do nothing;" % (
                q(g("ID")), org_sql, q(fam), q(g("Content Summary")), q(g("Hook Line (first 6s)")),
                q(g("Hook Angle")), q(g("Archetype")), q(g("Feature / Pillar")),
                q(g("Sport / League")), q(g("Format")), q(vd), q(g("Suggested CTA")),
                "true" if is_proven else "false", q(g("Delivery Date")),
            )
        )

    print("\n".join(out))


if __name__ == "__main__":
    args = [a for a in sys.argv[1:] if not a.startswith("--org-slug")]
    org_slug_arg = next((a.split("=", 1)[1] for a in sys.argv[1:] if a.startswith("--org-slug=")), "outlier")
    main(args[0], org_slug_arg)
